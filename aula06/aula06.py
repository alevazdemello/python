curso = " Curso de Python "

print(curso.lower())  # imprime tudo minusculo
print(curso.strip())  # imprime sem os espacos no inicio e no fim
print(curso.lower().strip())  # podemos concatenar funcoes
print(curso.upper().strip())  # .upper torna tudo maiusculo
print(curso.replace("Python", "C#"))  # altera
print(curso.split(" ")) #divide a variavel.neste caso como indicamos espaço, ele considera o espaço como quebra

print(curso[0])  # neste caso o [0] imprime apenas a letra C
print(curso[0:4])  # a delimitação pode ser definida no inicio e no fim
print("Tamanho : " + str(len(curso)))
